                <p>In 1996, Konrad Hambrecht became interested in Rooibos tea with its health benefits and potential for numerous applications. Out of his dislike for fizzy drinks and artificial products, a unique brew of Rooibos/fruit juice mixes was born, namely <strong>Rooibos Frutea</strong><sup>&reg;</sup>.</p>
